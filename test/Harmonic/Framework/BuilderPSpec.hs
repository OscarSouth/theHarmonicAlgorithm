    describe "Winner-trace + composing line (Phase 12)" $ do
      it "gen' + attempt 3 6 (strata) shows all bars in the Strata walk, not just the starter" $ do
        let cueE = H.initCadenceState 0 "E" [0,3,7]
        out <- captureStdout $ do
          _ <- seek "none" $ len 4 $ cue cueE $ entropy 0.3 $ attempt 3 6 $ genI'
          pure ()
        -- Each generated bar's per-step trace contributes one "strata "
        -- line. Pre-Phase-12 this would be 1 (starter only).
        let strataLines = length (filter ("    strata " `Data.List.isInfixOf`) (lines out))
        strataLines `shouldBe` 4

      it "gen'' + attempt 3 6 (strata) shows the Verbose-only soft-boost lines for the winner" $ do
        let cueE = H.initCadenceState 0 "E" [0,3,7]
        out <- captureStdout $ do
          _ <- seek "none" $ len 4 $ cue cueE $ entropy 0.3 $ attempt 3 6 $ genI''
          pure ()
        -- "boost" lines are emitted only by 'renderStrataStep' at
        -- verbosity 2, so their presence proves the K-attempt loop
        -- collected at Verbose, not Standard.
        out `shouldSatisfy` ("    boost " `Data.List.isInfixOf`)

      it "multi-attempt invocations open with the 'composing ..' line" $ do
        let cueE = H.initCadenceState 0 "E" [0,3,7]
        out <- captureStdout $ do
          _ <- seek "none" $ len 4 $ cue cueE $ entropy 0.3 $ attempt 3 6 $ gen
          pure ()
        out `shouldSatisfy` ("composing .." `Data.List.isInfixOf`)

      it "single-pass invocations do NOT print the 'composing ..' line" $ do
        let cueE = H.initCadenceState 0 "E" [0,3,7]
        out <- captureStdout $ do
          _ <- seek "none" $ len 4 $ cue cueE $ entropy 0.3 $ gen
          pure ()
        out `shouldNotSatisfy` ("composing .." `Data.List.isInfixOf`)

    it "attempt 3 12 picks progressions whose mean score beats the baseline mean" $ do
      -- Baseline: 30 single-pass attempts.
      let cueE = H.initCadenceState 0 "E" [0,3,7]
      baseline <- replicateM 30 (seek "none" $ len 8 $ cue cueE $ genVI)
      let baselineScores =
            map (PS.totalScore PS.defaultWeightsOffline . PS.scoreProgression) baseline
          meanBaseline = sum baselineScores / fromIntegral (length baselineScores)

      -- 8 multi-attempt picks.
      picks <- replicateM 8 (seek "none" $ len 8 $ cue cueE $ attempt 3 12 $ genVI)
      let pickScores =
            map (PS.totalScore PS.defaultWeightsOffline . PS.scoreProgression) picks
          meanPicks = sum pickScores / fromIntegral (length pickScores)

      -- 'best of 12' should pull the mean toward the upper tail.
      meanPicks `shouldSatisfy` (> meanBaseline)
